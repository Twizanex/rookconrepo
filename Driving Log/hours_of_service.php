<b>Day</b>
<ul>
    <li>A 24-hour period that begins at the hour designated by the carrier.</li>
    <li>Minimum 10 hours off-duty in the day.</li>
    <li>Maximum 13 hours driving in the day</li>
    <li>No driving after 14 hours on-duty in the day.</li>
</ul>

<b>Cycles</b>
<ul>
    <li>Drivers must select one of two cycles:<br />
    <b>Cycle 1:</b>
    <ul>
        <li>no driving after 70 hours on-duty in 7 days.</li>
    </ul>
    <b>Cycle 2:</b></li>
    <ul>
        <li>no driving after 120 hours in 14 days;</li>
        <li>driver must take at least 24 consecutive hours off-duty prior to accumulating 70 hours on-duty.</li>
    </ul>
</ul>

<b>Work-Shift</b>
<ul>
    <li>The total elapsed time between 2 off-duty hours is at least 8 consecutive hours.</li>
    <li>No driving after 16 hours of total elapsed time in the work-shift.</li>
    <li>Maximum 13 hours driving in the work-shift.</li>
    <li>No driving after 14 hours on-duty in the work-shift.</li>
</ul>

<b>Deferring Off-Duty Time</b>
<ul>
    <li>Drivers may reduce their off-duty requirement of 10 hours by up to 2 hours providing:</li>
    <ul>
        <li>the 2 hours are not part of the 8 consecutive off-duty hours on Day 1;</li>
        <li>the reduced time is added to the 8 consecutive off-duty hours taken on Day 2;</li>
        <li>additional 2 hours are also taken on Day 2;</li>
        <li>logbook clearly indicates "Deferral Used" and if 'Day 1' or 'Day 2'.</li>
    </ul>
</ul>

<b>Reset/Switch Cycles</b>
<ul>
    <li>A cycle can be reset or can switch cycles at any time by taking:</li>
    <ul>
        <li>36 consecutive hours off to reset Cycle 1 or switch from Cycle 1;</li>
        <li>72 consecutive hours off to reset Cycle 2 or switch from Cycle 2.</li>
    </ul>
</ul>

<b>Off-Duty</b>
<ul>
    <li>All drivers must take 24 consecutive off-duty hours every 14 days.</li>
</ul>

<b>Sleeper Berth</b>
<ul>
    <li>Same hours of service regulations apply for driving and on-duty limits;</li>
    <li>Work-shift limits on either side of each sleeper period are met;</li>
    <li>May split required off-duty hours into 2 periods if:<br />
    <b>Single Driver:</b>
    <ul>
        <li>neither period is less than 2 hours;</li>
        <li>total sleeper berth time is at least 10 hours in the day.</li>
    </ul>
    <b>Team Driver:</b>
    <ul>
        <li>neither sleeper period is less than 4 hours;</li>
        <li>total sleeper berth time is at least 8 hours;</li>
        <li>total off-duty time at least 10 hours in the day.</li>
    </ul>
</ul>